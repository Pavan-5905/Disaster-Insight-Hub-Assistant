import re

def clean_text(t):
    t = str(t).lower()
    t = re.sub(r"http\S+", "", t)      # remove urls
    t = re.sub(r"@\w+", "", t)         # remove mentions
    t = re.sub(r"#", "", t)            # remove hashtags symbol
    t = re.sub(r"[^a-zA-Z\s]", "", t)  # keep only letters
    t = re.sub(r"\s+", " ", t).strip()
    return t

