import gradio as gr

def gradio_predict_crisis(text):
    # The predict_crisis function must return the values to be unpacked here
    crisis_pred, final_location = predict_crisis(text)
    return f"Predicted Crisis Type: {crisis_pred}", f"Predicted Location: {final_location}"


